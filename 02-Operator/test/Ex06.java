public class Ex06 {

    public static void main(String[] args) {

        int i = 8;//十进制
        System.out.println(Integer.toBinaryString(i));
        int i1 = 010;//八进制
        System.out.println(Integer.toBinaryString(i1));
        int i2 = 0x8;//十六进制
        System.out.println(Integer.toBinaryString(i2));

    }
}
